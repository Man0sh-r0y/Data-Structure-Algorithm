public class BitWiseOperator {
    public static void main(String args[]) {
        // AND Operator
        System.out.println(5 & 6);
        // OR Operator
        System.out.println(5 | 6);
        // XOR Operator
        System.out.println(5 ^ 6);
        // NOT Operator
        System.out.println(~5);
        // Binary Left Shift
        System.out.println(5 << 2);
        // Binary Right Shift
        System.out.println(6 >> 1);
    }
}