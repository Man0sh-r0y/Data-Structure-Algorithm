public class SwitchStatement {
    public static void main(String[] args) {
        // Switch Statement
        int number = 2;
        switch (number) {
            case 1:
                System.out.println("Burgur");
                break;
            case 2:
                System.out.println("Mango Shake");
                break;
            case 3:
                System.out.println("Samosa");
                break;
            default:
                System.out.println("Nothing");
        }
    }
}
