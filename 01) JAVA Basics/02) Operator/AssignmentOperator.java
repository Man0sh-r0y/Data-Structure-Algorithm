public class AssignmentOperator {
    public static void main(String[] args) {
        int A;
        // Assignment Operator
        A = 10;
        A += 10;
        System.out.println(A);// 20
        A -= 10;
        System.out.println(A);// 10
        A *= 10;
        System.out.println(A);// 100
        A /= 10;
        System.out.println(A);// 10
    }
}
