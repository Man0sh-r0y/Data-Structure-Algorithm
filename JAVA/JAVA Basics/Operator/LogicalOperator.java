public class LogicalOperator {
    public static void main(String[] args) {
        // Logical Operator
        System.out.println((3 > 2) && (5 > 0)); // true
        System.out.println((3 < 2) && (5 > 0)); // false
        System.out.println((3 < 2) || (5 > 0)); // true
        System.out.println((3 > 2) || (5 > 0)); // true
    }
}
