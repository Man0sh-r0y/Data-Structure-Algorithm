public class JavaVariables {
    public static void main(String args[]) {
        System.out.println("Hello World");
        // Variables
        int a = 10;
        int b = 15;
        System.out.println(a);
        System.out.println(b);
        String name = "Tony Shark";
        System.out.println(name);
        a = 50;
        System.out.println(a);
        // Printing Stars
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");
        // Data Types in Java
        byte c = 8;
        System.out.println(c);
        char ch = 'c';// char can store single character
        System.out.println(ch);
        boolean var = false;
        float price = 10.5F;
        // long
        // double
        short n = 240;

        // sum of a & b
        a = 2;
        b = 5;
        int sum = a + b;
        System.out.println(sum);
    }
}