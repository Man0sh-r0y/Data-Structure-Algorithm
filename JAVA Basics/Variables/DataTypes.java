public class DataTypes {
    public static void main(String args[]) {
        // Variables
        // Data Types in Java

        // int Data Type
        int a = 10;
        int b = 15;
        System.out.println(a + " " + b);
        // String data Type
        String name = "Tony Shark";
        System.out.println(name);
        // byte data type
        byte c = 8;
        System.out.println(c);
        // char data type
        char ch = 'c';// char can store single character
        System.out.println(ch);
        // boolean data type
        boolean var = false;
        System.out.println(var);
        // float data type
        float price = 10.5F;
        System.out.println(price);
        // short data type
        short n = 240;
        System.out.println(n);
        // double data type
        double d = 15.2D;
        System.out.println(d);
        // log data type
        long l = 10000000L;
        System.out.println(l);
    }
}